fn cli() -> Command {
    Command::new("xtask")
        .about("turbo-tooling cargo tasks")
        .subcommand_required(true)
        .arg_required_else_help(true)
        .allow_external_subcommands(true)
        .subcommand(
            Command::new("npm")
                .about("Publish binaries to npm")
                .arg(arg!(<NAME> "the package to publish"))
                .arg_required_else_help(true),
        )
        .subcommand(
            Command::new("workspace")
                .arg(arg!(--publish "publish npm packages in pnpm workspace"))
                .arg(arg!(--bump "bump new version for npm package in pnpm workspace"))
                .arg(arg!(--"dry-run" "dry run all operations"))
                .arg(arg!([NAME] "the package to bump"))
                .about("Manage packages in pnpm workspaces"),
        )
        .subcommand(
            Command::new("nft-bench-result")
                .about("Print node-file-trace benchmark result against @vercel/nft"),
        )
        .subcommand(
            Command::new("upgrade-swc").about("Upgrade all SWC dependencies to the latest version"),
        )
        .subcommand(
            Command::new("summarize-benchmarks")
                .about(
                    "Normalize all raw data based on similar benchmarks, average data by \
                     system+sha and compute latest by system",
                )
                .arg(arg!(<PATH> "the path to the benchmark data directory")),
        )
        .subcommand(
            Command::new("print-rustflags")
                .about("Print the effective RUSTFLAGS for a given target triple")
                .long_about(
                    "Uses cargo's own config resolution to determine the merged rustflags \
                     for a target. This replaces the need for a separate rustflags.yml file.",
                )
                .arg(arg!(--target <TARGET> "the target triple (e.g. x86_64-unknown-linux-gnu)").required(true)),
        )
        .subcommand(
            Command::new("visualize-bundler-benchmarks")
                .about("Generate visualizations of bundler benchmarks")
                .long_about(
                    "Generates visualizations of bundler benchmarks. Currently supports:
    * Scaling: shows how each bundler scales with varying module counts

To generate the summary json file:
    * Check out this repository at the `benchmark-data` branch. An additional shallow clone or git \
                     worktree is recommended.
    * Run `cargo xtask summarize-benchmarks path/to/repo/data`
    * A summary file is generated within the data dir, e.g.  \
                     path/to/repo/data/ubuntu-latest-16-core.json

Visualizations generated by this command will appear in a sibling directory to the summary data \
                     file.",
                )
                .arg(arg!(<PATH_TO_SUMMARY_JSON> "the path to the benchmark summary json file"))
                .arg(arg!(--bundlers <BUNDLERS> "comma separated list of bundlers to include in the visualization")),
        )
}

/// Print the effective RUSTFLAGS for a given target using cargo's own config resolution.
///
/// Creates a temporary dummy crate and runs `cargo check --target <triple>` with a
/// RUSTC_WRAPPER that captures CARGO_ENCODED_RUSTFLAGS and aborts the build. This
/// ensures we get the exact same flag resolution cargo would use, including all
/// cfg()-based target matching.
fn print_rustflags(target: &str) -> Result<()> {
    use std::io::Write;

    let workspace_dir = var_os("CARGO_WORKSPACE_DIR")
        .map(PathBuf::from)
        .unwrap_or_else(|| current_dir().unwrap());

    let tmp_dir = std::env::temp_dir().join("xtask-rustflags");
    std::fs::create_dir_all(&tmp_dir)?;

    // Create a minimal dummy crate so cargo has something to "compile"
    let dummy_dir = tmp_dir.join("dummy");
    let dummy_src = dummy_dir.join("src");
    std::fs::create_dir_all(&dummy_src)?;
    std::fs::write(
        dummy_dir.join("Cargo.toml"),
        "[package]\nname = \"dummy\"\nversion = \"0.0.0\"\nedition = \"2021\"\n",
    )?;
    std::fs::write(dummy_src.join("lib.rs"), "")?;

    // Create a wrapper script that intercepts the rustc invocation.
    // With RUSTC_WRAPPER, cargo passes flags as CLI args (not CARGO_ENCODED_RUSTFLAGS).
    // The wrapper prints ALL args to stderr with a known prefix so we can extract
    // the flags that cargo appended beyond rustc's own args.
    //
    // For -vV queries: forward directly to real rustc.
    // For --print=* probes: forward to real rustc but strip rustflags (they're
    //   irrelevant for --print=file-names and can cause errors, e.g.
    //   -Clink-self-contained=+linker requires -Zunstable-options).
    // For actual compilation: capture all args and abort.
    let wrapper_path = tmp_dir.join("rustc-wrapper.sh");
    let marker = "XTASK_ARGS:";
    {
        let mut f = std::fs::File::create(&wrapper_path)?;
        write!(
            f,
            r#"#!/bin/sh
RUSTC="$1"; shift
# Check if this is a -vV or --print= query
IS_PROBE=0
IS_VV=0
for arg in "$@"; do
  case "$arg" in
    -vV) IS_VV=1 ;;
    --print=*) IS_PROBE=1 ;;
  esac
done
# Forward -vV directly
if [ "$IS_VV" = "1" ]; then
  exec "$RUSTC" "$@"
fi
# For --print= probes, forward to real rustc but strip resolved rustflags.
# Cargo appends rustflags to probe commands which can cause errors (e.g.
# -Clink-self-contained=+linker requires -Zunstable-options). The rustflags
# are irrelevant for probes, so we strip -C/-Z/--cfg flags.
if [ "$IS_PROBE" = "1" ]; then
  ARGS=""
  for arg in "$@"; do
    case "$arg" in
      -C*|-Z*|--cfg|--cfg=*) continue ;;
    esac
    ARGS="$ARGS $arg"
  done
  exec $RUSTC $ARGS
fi
# Actual compilation: capture args and abort
printf '{marker}%s\n' "$*" >&2
exit 1
"#,
        )?;
    }
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(&wrapper_path, std::fs::Permissions::from_mode(0o755))?;
    }

    // Run cargo check from the workspace root (so .cargo/config.toml is found)
    let output = process::Command::new("cargo")
        .arg("check")
        .arg("--target")
        .arg(target)
        .arg("--manifest-path")
        .arg(dummy_dir.join("Cargo.toml"))
        .env("RUSTC_WRAPPER", &wrapper_path)
        .current_dir(&workspace_dir)
        .stdout(process::Stdio::null())
        .stderr(process::Stdio::piped())
        .output()?;

    let stderr = String::from_utf8_lossy(&output.stderr);
    let args_line = stderr
        .lines()
        .find_map(|line| line.strip_prefix(marker))
        .ok_or_else(|| {
            anyhow::anyhow!("Failed to capture rustc args for target {target}.\nstderr: {stderr}")
        })?;

    // Extract rustflags from the args. Cargo appends resolved rustflags after its
    // own generated args (which end with `-L dependency=<path>`). The `-L` and
    // `dependency=...` are separate args. Rustflags start after the last `dependency=` arg.
    let flags: Vec<&str> = args_line.split_whitespace().collect();
    let last_dep_idx = flags
        .iter()
        .rposition(|a| a.starts_with("dependency="))
        .unwrap_or(0);
    let rustflags = &flags[last_dep_idx + 1..];
    let flags = rustflags.join(" ");
    let flags = flags.trim();

    println!("{flags}");

    // Clean up
    let _ = std::fs::remove_dir_all(&tmp_dir);

    Ok(())
}

fn main() -> Result<()> {
    let matches = cli().get_matches();
    match matches.subcommand() {
        Some(("npm", sub_matches)) => {
            let name = sub_matches
                .get_one::<String>("NAME")
                .expect("NAME is required");
            run_publish(name);
            Ok(())
        }
        Some(("workspace", sub_matches)) => {
            let is_bump = sub_matches.get_flag("bump");
            let is_publish = sub_matches.get_flag("publish");
            let dry_run = sub_matches.get_flag("dry-run");
            if is_bump {
                let names = sub_matches
                    .get_many::<String>("NAME")
                    .map(|names| names.cloned().collect::<FxHashSet<_>>())
                    .unwrap_or_default();
                run_bump(names, dry_run);
            }
            if is_publish {
                publish_workspace(dry_run);
            }
            Ok(())
        }
        Some(("nft-bench-result", _)) => {
            show_result();
            Ok(())
        }
        Some(("upgrade-swc", _)) => {
            let workspace_dir = var_os("CARGO_WORKSPACE_DIR")
                .map(PathBuf::from)
                .unwrap_or_else(|| current_dir().unwrap());
            let cargo_lock_path = workspace_dir.join("../../Cargo.lock");
            let lock = cargo_lock::Lockfile::load(cargo_lock_path).unwrap();
            let swc_packages = lock
                .packages
                .iter()
                .filter(|p| {
                    p.name.as_str().starts_with("swc_")
                        || p.name.as_str() == "swc"
                        || p.name.as_str() == "testing"
                })
                .collect::<Vec<_>>();
            let only_swc_set = swc_packages
                .iter()
                .map(|p| p.name.as_str())
                .collect::<FxHashSet<_>>();
            let packages = lock
                .packages
                .iter()
                .map(|p| (format!("{}@{}", p.name, p.version), p))
                .collect::<FxHashMap<_, _>>();
            let mut queue = swc_packages.clone();
            let mut set = FxHashSet::default();
            while let Some(package) = queue.pop() {
                for dep in package.dependencies.iter() {
                    let ident = format!("{}@{}", dep.name, dep.version);
                    let package = *packages.get(&ident).unwrap();
                    if set.insert(ident) {
                        queue.push(package);
                    }
                }
            }
            let status = process::Command::new("cargo")
                .arg("upgrade")
                .arg("--workspace")
                .args(only_swc_set)
                .current_dir(&workspace_dir)
                .stdout(process::Stdio::inherit())
                .stderr(process::Stdio::inherit())
                .status()
                .expect("Running cargo upgrade failed");
            assert!(status.success());
            let status = process::Command::new("cargo")
                .arg("update")
                .args(set.iter().flat_map(|p| ["-p", p]))
                .current_dir(&workspace_dir)
                .stdout(process::Stdio::inherit())
                .stderr(process::Stdio::inherit())
                .status()
                .expect("Running cargo update failed");
            assert!(status.success());
            Ok(())
        }
        Some(("summarize-benchmarks", sub_matches)) => {
            let path = sub_matches
                .get_one::<String>("PATH")
                .expect("PATH is required");
            let path = PathBuf::from(path);
            let path = path.canonicalize().unwrap();
            summarize_bench::process_all(path);
            Ok(())
        }
        Some(("print-rustflags", sub_matches)) => {
            let target = sub_matches
                .get_one::<String>("target")
                .expect("TARGET is required");
            print_rustflags(target)
        }
        Some(("visualize-bundler-benchmarks", sub_matches)) => {
            let path = sub_matches
                .get_one::<String>("PATH_TO_SUMMARY_JSON")
                .expect("PATH_TO_SUMMARY_JSON is required");
            let bundlers: Option<FxHashSet<&str>> = sub_matches
                .get_one::<String>("bundlers")
                .map(|s| s.split(',').collect());

            let path = PathBuf::from(path);
            let path = path.canonicalize().unwrap();
            visualize_bundler_bench::generate(path, bundlers)
        }
        _ => {
            anyhow::bail!("Unknown command {:?}", matches.subcommand().map(|c| c.0));
        }
    }
}
